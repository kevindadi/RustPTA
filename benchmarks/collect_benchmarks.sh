#!/usr/bin/env bash
set -euo pipefail

root="$(cd "$(dirname "$0")" && pwd)"
repo_root="$(cd "$root/.." && pwd)"
out_csv="$root/results/benchmark_metrics.csv"
tex_out="$root/results/benchmark_table.tex"

count_nodes() {
  local dot_file="$1"
  rg -n '^[[:space:]]*[0-9]+ \[' "$dot_file" | wc -l | tr -d ' '
}

parse_net_sizes() {
  local pn_dot="$1"
  local places transitions
  places=$(rg -n '^[[:space:]]*place_[0-9]+ \[' "$pn_dot" | wc -l | tr -d ' ')
  transitions=$(rg -n '^[[:space:]]*trans_[0-9]+ \[' "$pn_dot" | wc -l | tr -d ' ')
  echo "$places,$transitions"
}

parse_bug_count() {
  local json_file="$1"
  local key="$2"
  sed -nE "s/.*\"${key}\": ([0-9]+).*/\\1/p" "$json_file" | head -n1
}

collect_case() {
  local detector="$1"
  local stem="$2"

  local red_case_dir="$repo_root/tmp/bench_suite/${detector}_red/$stem"
  local raw_case_dir="$repo_root/tmp/bench_suite/${detector}_raw/$stem"

  local callable
  callable=$(count_nodes "$red_case_dir/callgraph.dot")

  local raw_net red_net
  raw_net=$(parse_net_sizes "$raw_case_dir/petrinet.dot")
  red_net=$(parse_net_sizes "$red_case_dir/petrinet.dot")

  local raw_places raw_trans red_places red_trans
  raw_places=$(echo "$raw_net" | cut -d',' -f1)
  raw_trans=$(echo "$raw_net" | cut -d',' -f2)
  red_places=$(echo "$red_net" | cut -d',' -f1)
  red_trans=$(echo "$red_net" | cut -d',' -f2)

  local raw_states red_states
  raw_states=$(count_nodes "$raw_case_dir/stategraph.dot")
  red_states=$(count_nodes "$red_case_dir/stategraph.dot")

  local bug_count
  case "$detector" in
    deadlock) bug_count=$(parse_bug_count "$red_case_dir/deadlock_report.txt.json" deadlock_count) ;;
    datarace) bug_count=$(parse_bug_count "$red_case_dir/datarace_report.txt.json" race_count) ;;
    atomic) bug_count=$(parse_bug_count "$red_case_dir/atomicity_report.txt.json" violation_count) ;;
  esac

  echo "$detector,$stem,$bug_count,$callable,$raw_places,$raw_trans,$raw_states,$red_places,$red_trans,$red_states" >> "$out_csv"
}

{
  echo 'detector,case,bug_count,callable_functions,raw_places,raw_transitions,raw_state_classes,reduced_places,reduced_transitions,reduced_state_classes'
} > "$out_csv"

for i in $(seq 1 10); do collect_case deadlock "dl_${i}"; done
for i in $(seq 1 10); do collect_case datarace "dr_${i}"; done
for i in $(seq 1 10); do collect_case atomic "at_${i}"; done

{
  echo '% Auto-generated by benchmarks/collect_benchmarks.sh'
  echo '\\begin{table*}[t]'
  echo '\\centering'
  echo '\\small'
  echo '\\begin{tabular}{llrrrrrrrr}'
  echo '\\toprule'
  echo 'Detector & Case & Bugs & Callables & Raw P & Raw T & Raw S & Red P & Red T & Red S \\\\ '
  echo '\\midrule'

  tail -n +2 "$out_csv" | while IFS=',' read -r detector case bug callable rp rt rs redp redt reds; do
    printf '%s & %s & %s & %s & %s & %s & %s & %s & %s & %s \\\\ \n' \
      "$detector" "$case" "$bug" "$callable" "$rp" "$rt" "$rs" "$redp" "$redt" "$reds"
  done

  echo '\\bottomrule'
  echo '\\end{tabular}'
  echo '\\caption{RustPTA benchmark metrics across detectors. Raw = no reduction, Red = with Petri-net reduction.}'
  echo '\\label{tab:rustpta-benchmarks}'
  echo '\\end{table*}'
} > "$tex_out"

echo "wrote: $out_csv"
echo "wrote: $tex_out"
